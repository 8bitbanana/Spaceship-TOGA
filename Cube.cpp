Cube::Cube()
{

}

Cube::Cube(glm::vec3 pos, glm::vec3 rotation, glm::vec3 size, std::string shader, std::string texture) :
	Position(pos), Rotation(rotation), Size(size), ShaderName(shader), TextureName(texture)
{
	Rotation = glm::radians(Rotation);

	Init();
}

void Cube::Init()
{
	shader = ResourceManager::GetShader(ShaderName);
	FaceTex = ResourceManager::GetTexture(TextureName);

	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);

	glBindVertexArray(VAO);

	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);
	// texture coord attribute
	glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);
}

void Cube::Draw(glm::mat4 projection, glm::mat4 view)
{
	shader.Use();
	glm::mat4 model = glm::mat4(1.0f);

	glm::quat rot = glm::quat(Rotation);
	model *= glm::toMat4(rot);

	model = glm::translate(model, Position);
	model = glm::scale(model, Size);

	shader.SetMatrix4("model", model);
	shader.SetMatrix4("projection", projection);
	shader.SetMatrix4("view", view);

	shader.SetInteger("texture1", 0);

	glActiveTexture(GL_TEXTURE0);
	FaceTex.Bind();

	glBindVertexArray(VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}
